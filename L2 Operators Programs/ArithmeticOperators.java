class ArithmeticOperators
{
	public static void main(String args[])
	{
		int a=10, b=7;
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a%b);

		System.out.println();

		double a1= 10, b1=7;
		System.out.println(a1+b1);
		System.out.println(a1-b1);
		System.out.println(a1*b1);
		System.out.println(a1/b1);
		System.out.println(a1%b1);
	}
}